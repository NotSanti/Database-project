CREATE TABLE Roles (
    roleId VARCHAR2(20) PRIMARY KEY,
    roleName VARCHAR2(40)
);

CREATE TABLE Contributors (
    contributorId VARCHAR2(20) PRIMARY KEY,
    fullName VARCHAR(50)
);

CREATE TABLE Song (
    songId VARCHAR2(20) PRIMARY KEY,
    releaseDate DATE,
    duration NUMBER(6)
);

CREATE TABLE Markets (
    marketId VARCHAR(20) PRIMARY KEY,
    area VARCHAR2(20)
);

CREATE TABLE RecordLabel (
    recordLabelId VARCHAR2(20) PRIMARY KEY,
    name VARCHAR2(25)
);

CREATE TABLE AuditLog (
    auditID VARCHAR2(20) PRIMARY KEY,
    userName VARCHAR2(20),
    dateInfo Date,
    information VARCHAR2(100)
);
    
CREATE TABLE RolesConSong (
    songId VARCHAR2(20),
    contributorId VARCHAR(20),
    roleId VARCHAR2(20),
    FOREIGN KEY (roleId) REFERENCES Roles(roleId) ON DELETE CASCADE,
    FOREIGN KEY (songId) REFERENCES Song(songId) ON DELETE CASCADE,
    FOREIGN KEY (contributorId) REFERENCES Contributors(contributorId) ON DELETE CASCADE,
    PRIMARY KEY(contributorId,roleId,songId)
);


CREATE TABLE Components (
    componentId VARCHAR(20) PRIMARY KEY,
    songId VARCHAR(20),
    offsetComponent NUMBER(10),
    durationComponent NUMBER(10),
    songUsed VARCHAR2(20),
    offsetSong NUMBER(10),
    durationSong NUMBER(10),
    FOREIGN KEY (songId) REFERENCES Song(songId) ON DELETE CASCADE,
    FOREIGN KEY (songUsed) REFERENCES Song(songId) ON DELETE CASCADE
);

CREATE TABLE Distribution (
    songId VARCHAR(20),
    recordLabelId VARCHAR2(20),
    marketId VARCHAR2(20),
    distributionDate Date,
    songTitle VARCHAR2(30),
    FOREIGN KEY (songId) REFERENCES Song(songId) ON DELETE CASCADE,
    FOREIGN KEY (recordLabelId) REFERENCES RecordLabel(recordLabelId) ON DELETE CASCADE,
    FOREIGN KEY (marketId) REFERENCES Markets(marketId) ON DELETE CASCADE
);

